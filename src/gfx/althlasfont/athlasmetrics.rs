// Standalone atlas lookup tables for the prebuilt PNG bucket font.
//
// The JSON atlas metadata still lives next to the textures, but the kernel
// path should not depend on parsing it at runtime. This file keeps a compact
// Rust-side lookup close to the atlas code: 8 bucket LUTs plus helpers that
// return both the bucket and slot for a given codepoint/char.

